    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        //The order in the function is 'name' -> value(not html value but key-value value) and 'id'->key
        $roles = Role::pluck('name', 'id')->all(); //Pulls out only specific data from the role model
        //We used lists(), but it was removed in Laravel 5.3 so now pluck() is used instead of lists()
        return view('admin.users.create', compact('roles'));
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(UsersRequest $request)
    {

        $input = $request->all();
        if ($file = $request->file('photo_id')) {
            $name = time() . $file->getClientOriginalName();
            $file->move('images', $name);
            $photo = Photo::create(['file' => $name]);
            $input['photo_id'] = $photo->id;
        }
        return redirect('/admin/users');

        //return $request->all();
    }

